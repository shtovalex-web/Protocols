# -*- coding: utf-8 -*-
"""Генерирует Word с ответами на типовые вопросы о программе (для передачи заказчику/ИБ)."""
from __future__ import annotations

from pathlib import Path

from docx import Document
from docx.shared import Pt

ROOT = Path(__file__).resolve().parent.parent
OUT = ROOT / "Описание_программы_ответы_на_вопросы.docx"


def main() -> None:
    doc = Document()
    normal = doc.styles["Normal"]
    normal.font.name = "Times New Roman"
    normal.font.size = Pt(12)

    doc.add_heading("Описание программы (ответы на типовые вопросы)", 0)
    note = doc.add_paragraph(
        "Текст сформирован по фактам из репозитория проекта (README, main.py, protocol_db.py, "
        "excel_data_cache.py). Не является юридической консультацией по 152-ФЗ и коммерческой тайне."
    )
    for r in note.runs:
        r.italic = True

    blocks: list[tuple[str, str]] = [
        (
            "1. На чём разработано",
            "Язык и рантайм: Python 3.10+ (в документации рекомендуют 3.11–3.12).\n"
            "Интерфейс: tkinter (настольное приложение под Windows).\n"
            "Основные библиотеки: openpyxl (Excel), python-docx (Word), fpdf; на Windows для PDF "
            "«как в Word» — Microsoft Word через COM (pywin32); опционально pymorphy2 для родительного "
            "падежа в протоколе.\n"
            "Сборка: PyInstaller (один .exe + файлы рядом), см. build_windows_exe.py.",
        ),
        (
            "2. Какая база данных используется",
            "SQLite, файл protocols.db (путь задаётся логикой приложения / пользовательского каталога).\n"
            "В БД: журнал сформированных протоколов, кэш справочников программ (листы B, PP, SIZ, V, V_PROF и "
            "т.п. из Excel), настройки (комиссия, приказ и др.).\n"
            "Список сотрудников как постоянная копия в SQLite не является основным источником: сотрудники "
            "читаются из Excel; в проекте явно указано, что персональные данные в кэше сотрудников не "
            "ведутся так же, как справочник программ.",
        ),
        (
            "3. Какой тип установки",
            "Отдельного установщика MSI в комплекте нет. Варианты: запуск py -3 main.py из папки проекта "
            "после pip install -r requirements.txt; либо портативная папка со сборкой PyInstaller — переносится "
            "вся папка с .exe и шаблонами. Рабочие файлы создаются рядом с программой или в каталоге "
            "пользовательских данных (app_paths.py).",
        ),
        (
            "4. Есть или будут ли обновления",
            "В коде нет встроенного автообновления с сервера. Обновления — вручную: новая версия из репозитория/"
            "архива, пересборка .exe, замена файлов комплекта. Для разработчиков есть скрипты вроде "
            "update_etalon.py (эталон для переноса), это не «обновление по кнопке» у конечного пользователя.\n"
            "Условия распространения и поддержка задаются владельцем репозитория; гарантии будущих релизов в "
            "коде не зафиксированы.",
        ),
        (
            "5. Как, какие данные и из каких баз используется",
            "Excel (Data_base.xlsx и др., пути настраиваются): сотрудники (листы вроде rabotnik и др.), при "
            "необходимости программы в той же книге.\n"
            "Excel (Programs_base.xlsx или листы в Data_base): справочники программ B, V_PROF, PP, SIZ, V и "
            "сопоставления для протокола и Минтруда.\n"
            "SQLite (protocols.db): журнал протоколов, кэш структур/данных программ из Excel, настройки.\n"
            "Word .docx: шаблон протокола (default_protocol.docx и др.).\n"
            "Файлы Минтруда: шаблон XSD/Excel для выгрузки; при работе с реестром — файл «Реестр обученных…» "
            "(локально).\n"
            "JSON: например last_protocol_no.json — последний номер протокола.\n"
            "Обмен с интернетом для этих данных в описанной архитектуре не заложен — опора на локальные файлы на ПК.",
        ),
        (
            "6. Доступ к конфиденциальной информации / коммерческой тайне",
            "Приложение не подключается к внешним системам по смыслу архитектуры и не запрашивает доступ к "
            "чужим базам: обрабатываются файлы, которые пользователь указал (Excel/Word), и локальная запись в "
            "protocols.db.\n"
            "Персональные данные могут содержаться в файлах пользователя и в журнале SQLite — зона "
            "ответственности оператора ПДн (работодателя), режима хранения и обработки на ПК.\n"
            "Коммерческая тайна — правовой статус конкретных сведений у организации. Программа не открывает "
            "доступ к тайне автоматически; если в Excel или протоколах вносятся сведения, составляющие "
            "коммерческую тайну, их защита — организационные и правовые меры плюс ограничение доступа к ПК и файлам.",
        ),
        (
            "7. Можно ли скомпилировать под Linux",
            "Полноценная «как на Windows» сборка под Linux одним переносом не гарантируется.\n"
            "Технически: из исходников на Linux можно поставить Python-зависимости и запускать main.py; "
            "PyInstaller умеет собирать бинарник под ту ОС, на которой запущен (на Linux — Linux-исполняемый файл).\n"
            "Ограничения: в проекте ориентация на Windows (пути, сценарии с Word через COM/pywin32, реестр/"
            "файлы Минтруда в типичной пользовательской среде). На Linux нет того же Word COM — PDF «как в Word» "
            "потребует доработки или упрощённого варианта (например, только текстовый PDF). Часть путей и "
            "инструкций в README завязана на Windows.\n"
            "Итог: запуск интерпретатором на Linux возможен после адаптации; готовой поддержки Linux в репозитории "
            "нет.",
        ),
    ]

    for title, body in blocks:
        doc.add_heading(title, level=1)
        for para in body.split("\n"):
            if para.strip():
                doc.add_paragraph(para.strip())

    doc.save(OUT)
    print(f"Записано: {OUT}")


if __name__ == "__main__":
    main()
